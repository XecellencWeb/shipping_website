'use client'


import {useState} from 'react'

const index = ({id}:{
    id:string
}) => {

    const [clipboard,setClipboard] = useState<string>('')






const copyToClip = ()=>{

    if(clipboard)return
    navigator.clipboard.writeText(id)

    setClipboard(id)

    setTimeout(()=>{
        setClipboard('')
    },5000)
}




  return (
    <div onClick={copyToClip} className='flex bg-pink-500 hover:bg-pink-800 py-2 gap-2 justify-center items-center rounded-lg px-4 cursor-pointer'>
    { !clipboard ?    <svg  xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="#fff" className="bi bi-copy" viewBox="0 0 16 16">
        <path fill-rule="evenodd" d="M4 2a2 2 0 0 1 2-2h8a2 2 0 0 1 2 2v8a2 2 0 0 1-2 2H6a2 2 0 0 1-2-2zm2-1a1 1 0 0 0-1 1v8a1 1 0 0 0 1 1h8a1 1 0 0 0 1-1V2a1 1 0 0 0-1-1zM2 5a1 1 0 0 0-1 1v8a1 1 0 0 0 1 1h8a1 1 0 0 0 1-1v-1h1v1a2 2 0 0 1-2 2H2a2 2 0 0 1-2-2V6a2 2 0 0 1 2-2h1v1z"/>
    </svg>
    :(
        <svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="#fff" className="bi bi-clipboard-check-fill" viewBox="0 0 16 16">
  <path d="M6.5 0A1.5 1.5 0 0 0 5 1.5v1A1.5 1.5 0 0 0 6.5 4h3A1.5 1.5 0 0 0 11 2.5v-1A1.5 1.5 0 0 0 9.5 0zm3 1a.5.5 0 0 1 .5.5v1a.5.5 0 0 1-.5.5h-3a.5.5 0 0 1-.5-.5v-1a.5.5 0 0 1 .5-.5z"/>
  <path d="M4 1.5H3a2 2 0 0 0-2 2V14a2 2 0 0 0 2 2h10a2 2 0 0 0 2-2V3.5a2 2 0 0 0-2-2h-1v1A2.5 2.5 0 0 1 9.5 5h-3A2.5 2.5 0 0 1 4 2.5zm6.854 7.354-3 3a.5.5 0 0 1-.708 0l-1.5-1.5a.5.5 0 0 1 .708-.708L7.5 10.793l2.646-2.647a.5.5 0 0 1 .708.708Z"/>
</svg>
    )}


        <p className="text-base text-white">{!clipboard ? 'Copy Id to Clipboard': 'Copied!'}</p>


    </div>
  )
}

export default index
